#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*new;
	unsigned char	i;

	if (!s || !f)
		return (NULL);
	i = 0;
	new = (char *)malloc(sizeof((char *)s + 1));
	if (new == NULL)
		return (NULL);
	while (s[i])
	{
		new[i] = f(i, (char)s[i]);
		i++;
	}
	new[i] = '\0';
	return (new);
}
