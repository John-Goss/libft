#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t cpt;

	cpt = 1;
	if (!s1 || !s2)
		return (0);
	while (*s1 == *s2 && cpt < n && *s1 && *s2)
	{
		s1++;
		s2++;
		cpt++;
	}
	if (*s1 != *s2)
		return (0);
	return (1);
}
